celcius = int(input("Enter the temperature in degree Celsius:\t"))

# Convert Celsius to Fahrenheit using the formula
fahrenheit = ((9/5) * celcius) + 32

print(f"Conversion of Celsius to Fahrenheit is {fahrenheit}.")


